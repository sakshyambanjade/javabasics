public class ladderif {
    public static void main(String[] args) {
        int m = 10;
        if (m > 90) {
            System.out.println("Grade A");
        } else if (m > 80) {
            System.out.println("Grade B");
        } else if (m > 70) {
            System.out.println("Grade B");
        } else if (m > 60) {
            System.out.println("Grade B");
        } else if (m > 50) {
            System.out.println("Grade B");
        } else if (m > 40) {
            System.out.println("Grade B");
        } else if (m > 30) {
            System.out.println("Grade B");
        } else if (m > 20) {
            System.out.println("Grade B");
        } else if (m > 10) {
            System.out.println("Grade B");
        } else {
            System.out.println("failed");
        }
    }
}