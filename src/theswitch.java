public class theswitch {
    public static void main(String[] arg) {
        String day = "123";
        switch (day) {
            case "sunday":
                System.out.println("it is funday");
                break;
            case "monday":
                System.out.println("it is munday");
                break;
            case "tuesday":
                System.out.println("it is tuesdayyy");
                break;
            default:
                System.out.println("It is work day :)");
        }
    }
}
